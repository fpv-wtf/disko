#include "mmsgui/3d/mms3dtorus.h"

MMS3DTorus::MMS3DTorus(MMS3DScene *scene,
						int material, int texture,
						int numwraps, int numperwrap, float majorradius, float minorradius) : MMS3DObject(scene, material, texture) {

	// get access to object's structure
	if (this->obj_id < 0) return;
	MMS3D_OBJECT *obj = this->scene->getObject(this->obj_id);

	// generate mesh
	this->scene->mms3dpm.genTorus(numwraps, numperwrap, majorradius, minorradius,
										&obj->vertices,
										&obj->normals,
										(obj->texture != MMS3DTexture::TEX_NOTSET)?&obj->texcoords:NULL,
										&obj->indices);
}
